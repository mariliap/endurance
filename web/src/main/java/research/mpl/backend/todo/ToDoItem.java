package research.mpl.backend.todo;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

@Entity
@Table(name = "todo_item")
@NamedQuery(
        name = "ToDoItem.findByUsername",
        query = "SELECT i FROM ToDoItem i WHERE i.username = :username")
@XmlRootElement
public class ToDoItem implements Serializable {

    private static final long serialVersionUID = 1L;

    @Id
    @GeneratedValue
    private Long id;

    private String username;

    @NotNull
    @Size(min = 5, max = 110,
            message = "Item description must be between 5 and 110 characters.")
    private String description;

    private boolean completed;

    /**
     * Default constructor
     */
    protected ToDoItem() {
        // Default constructor
    }

    /**
     * Constructor for creating instances
     *
     * @param username - username
     * @param description - description of the task
     * @param completed - completed
     */
    public ToDoItem(String username, String description, boolean completed) {
        this.username = username;
        this.description = description;
        this.completed = completed;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @XmlTransient
    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public boolean isCompleted() {
        return completed;
    }

    public void setCompleted(boolean completed) {
        this.completed = completed;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof ToDoItem)) {
            return false;
        }
        ToDoItem other = (ToDoItem) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "ToDoItem[ id=" + id
                + " username=" + username
                + " desciption=" + description
                + " completed=" + completed + " ]";
    }
}
